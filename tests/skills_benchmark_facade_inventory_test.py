def test_facade_preserves_pre_split_export_surface():
    """Every pre-split exported name must still be importable from the facade."""
    current = {name for name in dir(benchmark_insights) if not name.startswith("__")}
    missing = PRE_SPLIT_EXPORTS - current
    assert not missing, f"facade dropped re-exports: {sorted(missing)}"


def test_facade_exports_are_importable_by_name():
    """Guard the ``from benchmark_insights import X`` access path itself, not just dir()."""
    for name in PRE_SPLIT_EXPORTS:
        assert hasattr(benchmark_insights, name), f"missing attribute: {name}"
